export function drawMonogramLetter(
  setPixel: (x: number, y: number) => void,
  letter: string,
  originX: number,
  originY: number,
  scale: number,
): void {
  const glyph = GLYPHS[letter.toUpperCase()] ?? GLYPHS.T;
  for (let row = 0; row < glyph.length; row++) {
    const bits = glyph[row];
    for (let col = 0; col < 5; col++) {
      if ((bits >> (4 - col)) & 1) {
        for (let sy = 0; sy < scale; sy++) {
          for (let sx = 0; sx < scale; sx++) {
            setPixel(originX + col * scale + sx, originY + row * scale + sy);
          }
        }
      }
    }
  }
}
